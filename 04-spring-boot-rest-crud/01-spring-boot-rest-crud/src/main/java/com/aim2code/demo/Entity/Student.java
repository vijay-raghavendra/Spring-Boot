package com.aim2code.demo.Entity;

public class Student {

    private int  id;
    private String name;

    public Student() {
        System.out.println("Student constructor");
    }

    public Student(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
